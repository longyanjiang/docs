<h2 id="特点" tabindex="-1"><a class="header-anchor" href="#特点" aria-hidden="true">#</a> 特点</h2>
<p>Go语言是一门强类型的通用编程语言。它的基础语法与C语言很类似，但对于变量的声明有所不同，也对其他的一些优秀编程语言有所借鉴。另外，Go语言支持垃圾回收。</p>
<p>与C++相比，Go语言并不包括如异常处理、继承、泛型、断言、虚函数等功能，但增加了 Slice 型、并发、管道、垃圾回收、接口（Interface）等特性的语言级支持。</p>
<p>Go语言对并发编程的支持是天生的、自然的和高效的。Go语言为此专门创造出了一个关键字“go”。使用这个关键字，我们就可以很容易的使一个函数被并发的执行。</p>
<h2 id="撰写风格" tabindex="-1"><a class="header-anchor" href="#撰写风格" aria-hidden="true">#</a> 撰写风格</h2>
<p>在Go语言中有几项规定，而且这些是强制的，当不匹配以下规定时编译将会产生错误。</p>
<p>1.每行程序结束后不需要撰写分号;
2.大括号{不能够换行放置。
3.if 判断式和 for 循环不需要以小括号包覆起来。</p>
