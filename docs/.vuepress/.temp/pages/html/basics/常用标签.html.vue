<template><div><h3 id="常用标签" tabindex="-1"><a class="header-anchor" href="#常用标签" aria-hidden="true">#</a> 常用标签</h3>
<p>在学习标签之前我们先来认识一下标签，首先我们需要知道什么是标签</p>
<ol>
<li><strong>开始标签</strong>（Opening tag）：包含元素的名称，被大于号、小于号所包围。表示元素从这里开始或者开始起作用 —— 在本例中即段落由此开始。</li>
<li><strong>结束标签</strong>（Closing tag）：与开始标签相似，只是其在元素名之前包含了一个斜杠。这表示着元素的结尾 —— 在本例中即段落在此结束。初学者常常会犯忘记包含结束标签的错误，这可能会产生一些奇怪的结果。</li>
<li><strong>内容</strong>（Content）：元素的内容，本例中就是所输入的文本本身。</li>
<li><strong>元素</strong>（Element）：开始标签、结束标签与内容相结合，便是一个完整的元素。</li>
</ol>
<p><code v-pre>简单来说就是由大于号、小于号 &lt;&gt;组成的一个标签，并且分开始和结束，同时结束标签会多一个/，比如&lt;&gt;,&lt;/&gt;,这就是一对标签，在其中间便是标签的名称了，&lt;a&gt; &lt;a/&gt;,例如这样我们就称之为a标签，我们还可以简写为&lt;a/&gt;，前者我们称之为双标签，后者我们称之为单标签，这就是标签的组成。</code></p>
